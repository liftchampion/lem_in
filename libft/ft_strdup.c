#include <stdlib.h>
#include "libft.h"

char		*ft_strdup(const char *src)
{
	char *ptr;

	ptr = (char*)malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (!ptr)
		return (0);
	ft_strlcpy(ptr, src, ft_strlen(src) + 1);
	return (ptr);
}
