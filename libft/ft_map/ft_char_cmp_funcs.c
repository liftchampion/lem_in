#include "ft_types.h"

int		ft_cmp_char(void *a, void *b)
{
	return ((char)a - (char)b);
}

int		ft_cmp_str(void *v1, void *v2)
{
	unsigned char *s1;
	unsigned char *s2;

	s1 = v1;
	s2 = v2;
	while (*s1 && *s2 && *s1 == *s2)
	{
		++s1;
		++s2;
	}
	return (*s1 - *s2);
}
